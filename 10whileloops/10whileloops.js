// while loop
let n = prompt("Enter the value of n");
n = Number.parseInt(n);

let i = 10;
do {
  console.log(i);
  i++;
} while (i < n);

// Example 4: Using a While Loop
let count = 0;
while (count < 5) {
    console.log(count);
    count++;
}

// Example 5: Using a Do...While Loop
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 3);